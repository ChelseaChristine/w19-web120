<h2>Site vs App</h2>
<p>
  When it comes to the decision of either having a mobile application vs. a responsive page there is a lot to think about.<br>  
  Both have there issues when it comes to utilizing, but when it comes to the decision of which to use, it can depend on what <br> 
  you are trying to achieve.  Apps are nicer since they have a more obvious presence on a device, can hold more functionality <br> 
  compared to a website, and come with nice features like offline mode, pop up notifications on the device, and just generally <br> 
  a lot faster compared to a responsive mobile site.  The issue with apps though is they tend to cost much more in comparison <br> 
  to a website, and it takes longer to develop with a lot more steps compared to a website. Much is this is mentioned in the articles<br>
  by smashstack and hswsolutions.<br> 
  <br> 
  Now a responsive website still has its merit.  It can be viewed on multiple devices and tends to have better usability since more<br> 
  devices would be able to open it up compared to older phones that might not be able to have the application downloaded due to how <br> 
  old it is.  Responsive websites also take a lot less time to develop in comparison to an application and the cost is very much <br> 
  cheaper as it usually(but not always) involves just making the current site responsive.  One of the biggest downside to a responsive<br> 
  website though is the fact that loading can be very much longer and it can take more time to navigate compared to an application.<br> 
  <br>
  Responsive vs. Mobile?  It depends on what you want to achieve.  Can you imagine if a company like facebook only had a responsive website?<br> 
  I believe things more casual don't need an app, but in the end both are good and sometimes needed when it comes to specific applications,<br> 
  with help from the Apppresser article that also backs up the point when it involves utilizing both. <br> 
</p>
